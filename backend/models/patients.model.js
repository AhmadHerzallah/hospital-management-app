const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const patientsSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    age: {
      type: Number,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  },
);

const Patients = mongoose.model("Patients", patientsSchema);

module.exports = Patients;
